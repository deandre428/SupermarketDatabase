CREATE TABLE IF NOT EXISTS BRANCH(
	BranchID INT(11) UNSIGNED auto_increment NOT NULL UNIQUE PRIMARY KEY,
    City varchar(30) NOT NULL,
    Address varchar(100) NOT NULL,
    branchMSSN INT(9) UNSIGNED NOT NULL
);

CREATE TABLE IF NOT EXISTS OFFICE(
	officeNum INT NOT NULL AUTO_INCREMENT UNIQUE PRIMARY KEY,
    City VARCHAR(30) NOT NULL,
    Address varchar(100) NOT NULL,
    officeMSSN INT(9) UNSIGNED NOT NULL
);

CREATE TABLE IF NOT EXISTS EMPLOYEES(
	SSN INT(9) UNSIGNED NOT NULL AUTO_INCREMENT UNIQUE PRIMARY KEY,
    Name VARCHAR(30) NOT NULL,
    Address VARCHAR(100),
    Phone INT(10) UNSIGNED,
    DOB DATE,
    OfficeNum INT NOT NULL,
    BranchID INT(11) UNSIGNED NOT NULL,
    MSSN INT(9) UNSIGNED NOT NULL
    -- FOREIGN KEY (OfficeNum) REFERENCES OFFICE (officeNum),
    -- FOREIGN KEY (BranchID) REFERENCES BRANCH (BranchID)
);

CREATE TABLE IF NOT EXISTS MEMBERS(
	MemberID VARCHAR(15) NOT NULL UNIQUE PRIMARY KEY,
    Name VARCHAR(30) NOT NULL,
    Address VARCHAR(30),
    Phone INT(10) UNSIGNED,
    DOB DATE
);

CREATE TABLE IF NOT EXISTS PRODUCT(
	SKU VARCHAR(20) NOT NULL UNIQUE PRIMARY KEY,
    Description VARCHAR(30) NOT NULL,
    Price DECIMAL(19,2) UNSIGNED NOT NULL
);

CREATE TABLE IF NOT EXISTS FOOD(
	F_SKU VARCHAR(20) NOT NULL,
    ExpirationDate DATE,
    PRIMARY KEY (F_SKU, ExpirationDate),
    FOREIGN KEY (F_SKU) REFERENCES PRODUCT (SKU)
);

CREATE TABLE IF NOT EXISTS MEAT(
	meatSKU VARCHAR(20) NOT NULL,
    mType VARCHAR(20) NOT NULL,
    CUT VARCHAR(20),
    PRIMARY KEY (meatSKU, mType),
    FOREIGN KEY (meatSKU) REFERENCES FOOD (F_SKU)
);

CREATE TABLE IF NOT EXISTS PRODUCE(
	produceSKU VARCHAR(20) NOT NULL,
    pType VARCHAR(20) NOT NULL,
    PRIMARY KEY (produceSKU, pType),
    FOREIGN KEY (produceSKU) REFERENCES FOOD(F_SKU)
);

CREATE TABLE IF NOT EXISTS BREADS(
	breadsSKU VARCHAR(20) NOT NULL,
    bType VARCHAR(20) NOT NULL,
    PRIMARY KEY (breadsSKU, bType),
    FOREIGN KEY (breadsSKU) REFERENCES FOOD(F_SKU)
);

CREATE TABLE IF NOT EXISTS CLOTHING(
	C_SKU VARCHAR(20) NOT NULL,
    Color VARCHAR(15),
    Brand VARCHAR(30) NOT NULL,
    PRIMARY KEY (C_SKU, Brand),
    FOREIGN KEY (C_SKU) REFERENCES PRODUCT (SKU)
);

CREATE TABLE IF NOT EXISTS SHIRTS(
	shirtsSKU VARCHAR(20) NOT NULL,
    Button ENUM('Y','N') NOT NULL,
    Sleeve ENUM('L','S','N'),
    Size ENUM('S','M','L','XL'),
    PRIMARY KEY (shirtsSKU, Button),
    FOREIGN KEY (shirtsSKU) REFERENCES CLOTHING (C_SKU)
);

CREATE TABLE IF NOT EXISTS PANTS(
	pantsSKU VARCHAR(20) NOT NULL,
    pType VARCHAR(15) NOT NULL,
    Waist INT(2) UNSIGNED,
    Length INT(2) UNSIGNED,
    PRIMARY KEY (pantsSKU, pType)
 --   FOREIGN KEY (pantsSKU) REFERENCES CLOTHING (C_SKU)
);

CREATE TABLE IF NOT EXISTS SHOES(
	shoesSKU VARCHAR(20) NOT NULL,
    sType VARCHAR(15) NOT NULL,
    Size VARCHAR(6),
    PRIMARY KEY (shoesSKU, sType)
	-- FOREIGN KEY (shoesSKU) References CLOTHING (C_SKU)
);

CREATE TABLE IF NOT EXISTS ACCESSORIES(
	accessoriesSKU VARCHAR(20) NOT NULL,
    aType VARCHAR(15) NOT NULL,
    PRIMARY KEY (accessoriesSKU, aType)
    -- FOREIGN KEY (accessoriesSKU) REFERENCES CLOTHING (C_SKU)
);

CREATE TABLE IF NOT EXISTS ENTERTAINMENT(
	E_SKU VARCHAR(20) NOT NULL,
    releaseDate DATE NOT NULL,
    PRIMARY KEY (E_SKU, releaseDate)
    -- FOREIGN KEY (E_SKU) REFERENCES PRODUCT (SKU)
); 

CREATE TABLE IF NOT EXISTS GAMES(
	gamesSKU VARCHAR(20) NOT NULL,
    Title VARCHAR(20) NOT NULL,
    Publisher VARCHAR(20),
    Platform VARCHAR(10),
    PRIMARY KEY (gamesSKU, Title)
    -- FOREIGN KEY (gamesSKU) REFERENCES ENTERTAINMENT (E_SKU)
);
    
CREATE TABLE IF NOT EXISTS MUSIC(
	musicSKU VARCHAR(20) NOT NULL,
    Title VARCHAR(15) NOT NULL,
    Genre VARCHAR(15),
    Artist VARCHAR(10),
    PRIMARY KEY (musicSKU, Title)
    -- FOREIGN KEY (musicSKU) REFERENCES ENTERTAINMENT (E_SKU)
);

CREATE TABLE IF NOT EXISTS MOVIES(
	moviesSKU VARCHAR(20) NOT NULL,
    Title VARCHAR(30) NOT NULL,
    Genre VARCHAR(20),
    Director VARCHAR(10),
    PRIMARY KEY (moviesSKU, Title)
    -- FOREIGN KEY (moviesSKU) REFERENCES ENTERTAINMENT (E_SKU)
);

CREATE TABLE IF NOT EXISTS BOOKS(
	bookSKU VARCHAR(20) NOT NULL,
    Title VARCHAR(30) NOT NULL,
    Author VARCHAR(30),
    Publisher VARCHAR(30),
    PRIMARY KEY (bookSKU, Title)
    -- FOREIGN KEY (bookSKU) REFERENCES ENTERTAINMENT (E_SKU)
);

CREATE TABLE IF NOT EXISTS HAS(
	BranchID INT(11) UNSIGNED NOT NULL,
